def estimate_sizes(variant):
    """Быстрая оценка размеров варианта"""
    # Диаметры венцов (делительный + стенка ~3m)
    d_ring1 = variant['m1'] * variant['z_r1'] + 6 * variant['m1']
    d_ring2 = variant['m2'] * variant['z_r2'] + 6 * variant['m2']
    d_max = max(d_ring1, d_ring2)

    # Осевой габарит
    b_total = variant['b1'] + variant['b2']
    lax = b_total + 30  # + место на водила/подшипники

    # Выходной момент (оценка)
    i_total = variant['i1'] * variant['i2']
    t_out_est = 3.7 * i_total * (0.9 ** 2)  # η ≈ 0.9

    return {
        'd_max': d_max,
        'b_total': b_total,
        'lax': lax,
        't_out_est': t_out_est,
        'i_total': i_total,
    }

